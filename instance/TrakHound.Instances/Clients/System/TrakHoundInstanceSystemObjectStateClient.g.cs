        public async Task<IEnumerable<ITrakHoundObjectStateEntity>> LatestByObject(
            string path,
            string routerId = null)
        {
            var router = BaseClient.GetRouter(routerId);
            if (router != null)
            {
                var response = await router.Entities.Objects.States.LatestByObject(new string[] { path });
                if (response.IsSuccess)
